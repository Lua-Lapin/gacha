export const PROMPT_TEMPLATE = `添付したアバターを元に、正方形のカクテルアイコン風イラストを作成してください。

カクテル名は「{カクテル名}」です。
これは「形容詞 + 実在カクテル名」の架空カクテルです。

カクテルの実在部分から、ベースとなるグラス形状・液色・ガーニッシュの特徴を反映してください。
さらに、形容詞の意味に合わせて大胆にアレンジしてください。

必ず以下を変化させてください：
1. グラス形状
2. 液体の色
3. ガーニッシュ
4. 氷・泡・煙・光の演出
5. 背景の雰囲気
6. タイトル文字のフォント感

アバターは元画像の特徴を維持してください。
髪型・髪色・顔立ち・瞳の色・服装・アクセサリーなど、元画像の見た目の印象をそのまま残してください。

構図（正方形アイコン）：
- 顔と上半身を中央に大きく配置
- 片手でカクテルを持つ
- 顔とカクテルの両方が主役
- カクテルは手前に少し大きく配置
- 被写体を画面いっぱいに収め、余白を作りすぎない
- 背景は浅い被写界深度でおしゃれにぼかす

下部には「{カクテル名}」を大きく配置してください。
文字はカクテルの雰囲気に合わせて装飾し、読みやすくしてください。
日本語の文字は正確にしてください。

全体は高品質なアニメ調、華やかなライティング、キラキラしたアイコン風にしてください。`

export function buildPrompt(title) {
  return PROMPT_TEMPLATE.replaceAll('{カクテル名}', title)
}
